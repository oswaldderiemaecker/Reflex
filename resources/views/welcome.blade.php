    <h2>¿Qué hay de nuevo?</h2>

    <p>Reflex 360º contiene una serie de herramientas que llevara el control de la visita medica a otro nivel:</p>

    <ul>
        <li>Cliente Android aplicación 100% nativa con gran rendimiento y performance.</li>
        <li>Administración de países, empresas, unidades y sub unidades de negocio, zonas geográficas, parrillas promocionales, usuarios (administradores, marketing, supervisión, field force), tipos de cliente</li>
